    public void guardaArchivo(){
    
        String query = txtsqlQuery.getText();
        
        try{
            
            JFileChooser guardarComo = new JFileChooser();
            guardarComo.setApproveButtonText("Guardar");
            guardarComo.showSaveDialog(null);
            File archivo = new File(guardarComo.getSelectedFile()+".sql");
            
            BufferedWriter salida = new BufferedWriter(new FileWriter(archivo));
            salida.write(query);
            salida.close();
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, 
                    "Error al guardar fichero", "Error", 
                    JOptionPane.ERROR_MESSAGE);    
        }
        finally{
            
            JOptionPane.showMessageDialog(null, "Tarea realizada con exito!");
        }
    }
